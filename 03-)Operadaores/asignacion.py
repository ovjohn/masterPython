#Operadores de Asignacion
edad = 40 # Asignacion a la variable edad el numero 40
print(edad)

edad += 6 #Se esta asignando a la variable edad 6 mas "Es ugal a decir: edad + 6"
print(edad)

edad -=10 #Igual al anterior pero se resta
print(edad)


